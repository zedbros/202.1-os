/// Swaps the contents of `xs[p]` and `xs[q]`.
void exchange(int* xs, int p, int q);

/// Reverses the contents of the array pointed to by `xs`. which contains `length` elements.
void reverse(int* xs, int length);

/// Sorts the elements in the array pointed to by `xs`, which contains `length` elements.
void quicksort(int* xs, int length);
